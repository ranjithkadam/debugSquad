//Data Types: it defines which type of data stored in variable or memory, to check dataType "typeof(varName)" used.
//In JavaScript there are two types of data types:

// 1.primitive data type

// Primitive Data types: The primitive data type is an immutable/ unchange data during execution.
//There are majorly 5 types:
// 1.Number, 2.string, 3.Boolean, 4.undefine, 5.null, 6.BigInt

//1.Number

let number=10;
console.log( number, typeof(number));

//2.String
let str="Hello World";
console.log(str, typeof(str));

//3.boolean
let bool=true;
console.log(bool, typeof(bool));

//4. undefined
let name;
console.log(name, typeof(name));

//5. null
name=null;
 console.log(name, typeof(name));

 //6.Bigint
 let big=95678909876545609;
